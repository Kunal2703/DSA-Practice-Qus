FROM `CUSTOMERS`
INNER JOIN `ORDERS`
ON CUSTOMERS.ID = ORDERS.`CUSTOMER_ID`
INNER JOIN `EMPLOYEE`
ON `ORDERS`.`OID` = `EMPLOYEE`.`EID`;



