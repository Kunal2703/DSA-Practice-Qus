-- Inner Join

-- 2 TABLE
SELECT c.ID, c.NAME, o.AMOUNT, o.DATE
FROM `CUSTOMERS` as c
INNER JOIN `ORDERS` as o
ON c.ID = o.`CUSTOMER_ID`;


-- 3 TABLE
